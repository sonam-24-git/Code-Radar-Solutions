#include<stdio.h>
int main(){
    int a,b,c;
    scanf("%d",&a);
    scanf("%d",&b);
    scanf("%d",&c);
    int add,sub,mul,div;
    if(add=c){
        printf("%d",a+b);
    }else if(sub=c){
        printf("%d",a-b);
    }else if(mul=c){
        printf("%d",a*b);
    }else if(div=c){
        printf("%d",a/b);
    
    }
    return 0;
}